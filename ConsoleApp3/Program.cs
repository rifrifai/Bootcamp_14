// bitwise operators
int a = 13, b = 25, result;
// bitwise AND operator
result = a & b;
Console.WriteLine($"Bitwise AND: {result}");
// bitwise OR operator
result = a | b;
Console.WriteLine($"Bitwise OR: {result}");
// bitwise XOR operator
result = a ^ b;
Console.WriteLine($"Bitwise XOR: {result}");


// relational operators
bool isResult;
int num1 = 10, num2 = 30;
Console.WriteLine(num1);
Console.WriteLine(num2);
// equal to operator
isResult = (num1 == num2);
Console.WriteLine($"equal (=) to operator {isResult}");
// greater than operator
isResult = (num1 > num2);
Console.WriteLine($"greater than (>) operator: {isResult}");
// less than operator
isResult = (num1 < num2);
Console.WriteLine($"less than (<) operator: {isResult}");
// greater than equal to operator
isResult = (num1 >= num2);
Console.WriteLine($"greater than or equal to (>=) operator: {isResult}");
// less than equal to operator
isResult = (num1 <= num2);
Console.WriteLine($"less than or equal to (<=) operator: {isResult}");
// not equal to operator
isResult = (num1 != num2);
Console.WriteLine($"not equal to (!=) operator: {isResult}");
